var turno =( prompt("Em que turno você estuda? Digite 'M' para matutino, 'V' para vespertino ou 'N' para noturno:"));

if (turno === 'M')
     {
    alert("Bom dia!");

} else if (turno === 'V') {

    alert("Boa tarde!");

} else if (turno === 'N') {

    alert("Boa noite!");


} else {
    alert("Opção inválida! Por favor, digite 'M', 'V' ou 'N'.");


}