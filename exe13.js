var numero = parseInt(prompt("Digite um número entre 1 e 7 para saber o dia da semana:"));


if (numero === 1) {

    alert("Domingo");

} else if (numero === 2) {

    alert("Segunda-feira");

} else if (numero === 3) {

    alert("Terça-feira");

} else if (numero === 4) {

    alert("Quarta-feira");

} else if (numero === 5) {
    
    alert("Quinta-feira");

} else if (numero === 6) {

alert("Sexta-feira");

} else if (numero === 7) {

alert("Sábado");

} else {
    alert("Valor inválido");
}